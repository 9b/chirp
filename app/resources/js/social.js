function build_social_button(service, url, text) {
    var shell, svg, d1, d2;
    svg = $('<svg></svg>')
          .attr({'xmlns': 'http://www.w3.org/2000/svg',
                 'viewBox': '0 0 24 24'})
          .append($('<path></path>')
                  .attr({'d': SERVICES[service].svg}));
    d1 = $('<div></div>')
         .addClass('resp-sharing-button__icon resp-sharing-button__icon--solidcircle')
         .append(svg);
    d2 = $('<div></div>')
         .addClass('resp-sharing-button resp-sharing-button--' +  service + ' resp-sharing-button--small')
         .append(d1);
    var href = SERVICES[service].url
              .replace('XX-URL-XX', url)
              .replace('XX-TEXT-XX', text);
    shell = $('<a></a>')
              .addClass('resp-sharing-button__link')
              .attr({'href': href, 'target': '_blank'})
              .append(d2);
    return shell;
}
